package Section3;

public class StatementsWhiteSpaceIndenting {
    public static void main(String[] args) {

        // STATEMENTS

        int myVariable = 50;
        // expression: myVariable = 50
        // statement: int myVariable = 50; --> entire line
        myVariable++; // --> statement
        myVariable--; // --> statement

        System.out.println("This is a test");
        // statement: System.out.println("This is a test");
        // expression: "This is a test"


        System.out.println("This is " +
                "also another " +
                "test"); // all this is a statement too



        // INDENTING - to make code more readable

    }
}
