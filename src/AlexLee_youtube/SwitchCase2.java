package AlexLee_youtube;

public class SwitchCase2 {
    public static void main(String[] args) {

        String dog = "pomeranian";

        switch (dog) {
            case "pomeranian":
                System.out.println("small");
                break;
            case "great dane":
                System.out.println("large dog");
                break;
            default:
                System.out.println("Try different dog");
                break;
        }


    }
}
