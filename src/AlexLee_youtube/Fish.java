package AlexLee_youtube;

public class Fish {
    String breed;
    String name;
    int age;
    double weight;

    public Fish setBreed(String breed) {
        this.breed = breed;
        return this;
    }

    public Fish setName(String name) {
        this.name = name;
        return this;
    }

    public Fish setAge (int age) {
        this.age = age;
        return this;
    }

    public Fish setWeight (double weight) {
        this.weight = weight;
        return this;
    }

}
