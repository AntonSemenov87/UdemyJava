package AlexLee_youtube.someYoutubeCourse;


public class Loops {
    public static void main(String[] args) {

        int num = 12;

        while (num < 18) {
            System.out.println("You need to grow up, wait");
            num++;
            System.out.println("1 year has passed");
        }

        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        int x = 0;
        do {
            System.out.println("Happy 4th of July!");
            x++;
        } while (x < 5);


    }
}
